def _coerce_arg(raw, arg_spec):
    arg_type = str(arg_spec.get("type", "str")).lower()
    if raw is None:
        return None
    if arg_type == "int":
        return int(raw)
    if arg_type == "float":
        return float(raw)
    if arg_type == "bool":
        return str(raw).strip().lower() in {"1", "true", "yes", "on"}
    return str(raw)


def _dispatch_dynamic_registered_tool(repo_path, tool_name, tool_args):
    meta = TOOL_REGISTRY.get(tool_name)
    if not meta:
        return None

    category = meta.get("category")
    if not category:
        return 3, {"error": "invalid_registry_entry", "tool": tool_name, "detail": "missing category"}

    try:
        try:
            module = importlib.import_module(taxonomy_import_module(category, tool_name))
        except Exception:
            module = importlib.import_module(f"tools.{category}.{tool_name}.command")
    except Exception as exc:
        return 3, {
            "error": "dynamic_import_failed",
            "tool": tool_name,
            "detail": str(exc),
        }

    func = getattr(module, f"cmd_{tool_name}", None)
    if not callable(func):
        return 3, {
            "error": "missing_command_function",
            "tool": tool_name,
            "detail": f"Expected callable cmd_{tool_name}",
        }

    parsed_args = []
    specs = meta.get("args", []) if isinstance(meta.get("args", []), list) else []
    for idx, spec in enumerate(specs):
        raw = tool_args[idx] if idx < len(tool_args) else None
        is_optional = bool(spec.get("optional", False))
        default = spec.get("default", None)

        if raw is None:
            if is_optional:
                parsed_args.append(default)
                continue
            return 2, {
                "error": "missing_argument",
                "tool": tool_name,
                "detail": f"missing required arg: {spec.get('name', f'arg_{idx}')}",
            }

        try:
            parsed_args.append(_coerce_arg(raw, spec))
        except ValueError:
            return 2, {
                "error": "invalid_argument",
                "tool": tool_name,
                "detail": f"invalid value for {spec.get('name', f'arg_{idx}')}: {raw}",
            }

    try:
        return func(repo_path, *parsed_args)
    except Exception as exc:
        return 3, {
            "error": "runtime_exception",
            "tool": tool_name,
            "detail": str(exc),
        }


def _parse_int_arg(tool_args, index, tool_name, arg_name, default=None):
    """Parse an optional integer argument by position.  Returns the value (or default),
    or an error tuple (exit_code, payload) if the value is present but invalid."""
    if index >= len(tool_args):
        return default
    try:
        return int(tool_args[index])
    except ValueError:
        print(f"{tool_name} {arg_name} must be an integer", file=sys.stderr)
        return 2, {"error": "invalid_argument", "tool": tool_name, "detail": f"{arg_name} must be integer"}


def _parse_report_style_args(tool_args, default_top_n):
    output_format = "json"
    category_filter = None
    top_n = default_top_n

    args = list(tool_args)
    if args and args[0] in {"json", "table"}:
        output_format = args.pop(0)

    if args:
        try:
            top_n = int(args[0])
            args.pop(0)
        except ValueError:
            category_filter = args.pop(0)

    if args:
        try:
            top_n = int(args[0])
        except ValueError:
            return 2, {"error": "invalid_argument", "detail": "top_n must be integer"}

    return output_format, category_filter, top_n


def cmd_list_tools(category_filter=None):
    """List all tools, optionally filtered by category."""
    import json
    
    output = {
        "all_categories": list(TOOL_CATEGORIES.keys()),
        "categories": {}
    }
    
    for category_name, category_info in TOOL_CATEGORIES.items():
        # If filter requested, skip other categories
        if category_filter and category_name != category_filter:
            continue
            
        tools_list = []
        for tool_name in category_info.get("tools", []):
            tool_meta = TOOL_REGISTRY.get(tool_name, {})
            tools_list.append({
                "name": tool_name,
                "description": tool_meta.get("description", ""),
                "category": tool_meta.get("category", "unknown")
            })
        
        output["categories"][category_name] = {
            "description": category_info.get("description", ""),
            "tool_count": len(tools_list),
            "tools": tools_list
        }
    
    print(json.dumps(output, indent=2))
    return 0


def main():
    # Handle special commands
    if len(sys.argv) > 1:
        if sys.argv[1] in ("--help", "-h", "help"):
            return cmd_help()
        
        # NEW: Handle list-tools with optional category filter
        if sys.argv[1] == "list-tools":
            category_filter = sys.argv[2] if len(sys.argv) > 2 else None
            return cmd_list_tools(category_filter=category_filter)

    parser = argparse.ArgumentParser(description="mini-codex ai_repo_tools dispatcher", add_help=False)
    parser.add_argument("tool")
    parser.add_argument("args", nargs="*")
    args = parser.parse_args()

    repo_path = os.getcwd()
    try:
        exit_code, payload = dispatch_tool(repo_path, args.tool, args.args)
    except Exception as exc:
        print(f"tool runtime error: {exc}", file=sys.stderr)
        exit_code, payload = 3, {"error": "runtime_exception", "detail": str(exc)}

    if exit_code == 0:
        stderr_for_obs = ""
    else:
        parts = []
        if isinstance(payload, dict):
            error_key = payload.get("error", "")
            detail = payload.get("detail", "")
            target = payload.get("target", "")
            if error_key == "file_not_found":
                parts.append("file not found")
                if target:
                    parts.append(target)
            elif error_key == "missing_argument":
                parts.append("missing argument")
            elif error_key == "timeout":
                parts.append("timeout")
            elif error_key == "runtime_exception" and detail:
                parts.append(detail)
            elif error_key:
                parts.append(error_key.replace("_", " "))
                if detail:
                    parts.append(detail)
            # For cmd_run failures: use stderr_excerpt then stdout_excerpt as signal
            if not parts and args.tool == "cmd_run":
                stderr_ex = payload.get("stderr_excerpt", "")
                stdout_ex = payload.get("stdout_excerpt", "")
                parts.append(stderr_ex or stdout_ex)
        stderr_for_obs = "; ".join(filter(None, parts))

    observe_tool_run(
        repo_path=repo_path,
        tool_name=args.tool,
        tool_args=args.args,
        exit_code=exit_code,
        payload=payload,
        stderr_text=stderr_for_obs,
    )

    return exit_code


if __name__ == "__main__":
    raise SystemExit(main())
